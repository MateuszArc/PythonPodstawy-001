#W tym zadaniu liczymy pole koła
WartoscPi = 3.14
PromienOkregu = 5
PoleKola = WartoscPi * PromienOkregu ** 2
print("Pole koła wynosi: ", PoleKola)
#Obwód Ogręgu:
WartoscPi2 = 4.15
PromienOkregu2 = 8
Obwod_Okregu = 2 * WartoscPi2 * PromienOkregu2
print("Obwód okręgu wynosi:", Obwod_Okregu)
#Pole prostokąta:
